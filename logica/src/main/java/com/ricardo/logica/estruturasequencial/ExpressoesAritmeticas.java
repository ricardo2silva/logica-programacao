package com.ricardo.logica.estruturasequencial;

public class ExpressoesAritmeticas {
    public static void main(String[] args) {

        //soma
        System.out.println(10 + 5);

        //divisao
        System.out.println(10 / 5);

        //multiplicacao
        System.out.println( 10 * 5);

        //subtracao
        System.out.println(10 - 5);

        //resto divisao
        System.out.println( 10 % 5);

        //precedencia matematica * / %  + -
    }


}
